  return lhs == rhs;
}

+ (NSDictionary<NSString *, NSDictionary<NSString *, id> *> *)markdownStyles:
    (facebook::react::RTNMarkdownMarkdownStylesStruct)reactMarkdownStyles {
  return @{
    @"h1" : @{
      @"backgroundColor" :
              RCTUIColorFromSharedColor(reactMarkdownStyles.h1.backgroundColor)
          ?: [NSNull null],
      @"color" : RCTUIColorFromSharedColor(reactMarkdownStyles.h1.color)
          ?: [NSNull null],
      @"fontFamily" :
              RCTNSStringFromStringNilIfEmpty(reactMarkdownStyles.h1.fontFamily)
          ?: [NSNull null],
      @"fontSize" : reactMarkdownStyles.h1.fontSize
          ? @((CGFloat)reactMarkdownStyles.h1.fontSize)
          : [NSNull null],
      @"fontStyle" :
              RCTNSStringFromStringNilIfEmpty(reactMarkdownStyles.h1.fontStyle)
          ?: [NSNull null],
      @"fontWeight" :
              RCTNSStringFromStringNilIfEmpty(reactMarkdownStyles.h1.fontWeight)
          ?: [NSNull null],
    },
  };
}

